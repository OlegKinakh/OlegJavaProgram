package com.java.class13;
// Take one String and two int variables
// Print addition of 2 numbers with concatenation of with string
public class StringConcatenationAssignment {
    public static void main(String[] args) {
        String s1 = "Hello";
        int num1 = 7;
        int num2 = 14;

        System.out.println(s1 + (num1+num2));
        System.out.println(num1 + num2 + s1 + num1+num2);


    }
}
